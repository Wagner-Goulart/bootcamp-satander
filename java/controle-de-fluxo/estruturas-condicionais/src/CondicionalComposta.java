public class CondicionalComposta {
    public static void main(String[] args) {
        int nota = 9;

        // CONDICIONAL
        if(nota >= 7) {
            System.out.println("Aprovado"); // EXECUTA QUANDO O RESULTADO É TRUE
        } else {
            System.out.println("Reprovado"); // EXECUTA QUANDO O RESULTADO É FALSE
        }
    }
}
